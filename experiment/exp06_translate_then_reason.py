def plot_recovery_heatmap(
    recovery: Dict[str, Dict[str, float]],
    output_path: str = "exp06_recovery_heatmap.png",
):
    """
    Heatmap: Recovery Score per (language × dimension).
    Green=high recovery (T→R matches English), Red=low (no effect).
    """
    langs = [l for l in LANGS_TO_EVAL if l in recovery and l != "en"]
    dims  = DIMENSIONS

    matrix = np.full((len(langs), len(dims)), float("nan"))
    for i, lang in enumerate(langs):
        for j, dim in enumerate(dims):
            v = recovery.get(lang, {}).get(dim, float("nan"))
            matrix[i, j] = v

    fig, ax = plt.subplots(figsize=(11, 6))
    im = ax.imshow(matrix, cmap="RdYlGn", vmin=0, vmax=1, aspect="auto")
    ax.set_xticks(range(len(dims)))
    ax.set_xticklabels(dims, rotation=30, ha="right", fontsize=9)
    ax.set_yticks(range(len(langs)))
    ax.set_yticklabels(langs, fontsize=9)
    plt.colorbar(im, ax=ax, label="Recovery Score (1=full English behavior)")
    ax.set_title(
        "Exp06 T→R: Recovery Score — how much bias recovered by translating to English",
        fontsize=11,
    )
    for i in range(len(langs)):
        for j in range(len(dims)):
            v = matrix[i, j]
            if not np.isnan(v):
                ax.text(j, i, f"{v:.2f}", ha="center", va="center",
                        fontsize=7, color="black")
    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"  Recovery heatmap saved → {output_path}")


def plot_comparison_bar(
    tr_pref: Dict[str, Dict[str, float]],
    native_pref: Dict[str, Dict[str, float]],
    human_df: pd.DataFrame,
    focus_langs: List[str],
    output_path: str = "exp06_comparison_bar.png",
):
    """
    For focus languages (id, ja, de — most extreme in exp01):
    Bar chart per dimension: Native vs T→R vs Human
    """
    human_mean = human_df.groupby("Label")["human_pct"].mean()

    n_langs = len(focus_langs)
    fig, axes = plt.subplots(1, n_langs, figsize=(5 * n_langs, 5), sharey=True)
    if n_langs == 1:
        axes = [axes]

    x = np.arange(len(DIMENSIONS))
    w = 0.25

    for ax, lang in zip(axes, focus_langs):
        native_vals = [native_pref.get(lang, {}).get(d, 50.0) for d in DIMENSIONS]
        tr_vals     = [tr_pref.get(lang, {}).get(d, 50.0)     for d in DIMENSIONS]
        human_vals  = [human_mean.get(d, 50.0)                for d in DIMENSIONS]

        ax.bar(x - w, native_vals, width=w, label="Native (exp01)", color="#e74c3c", alpha=0.8)
        ax.bar(x,     tr_vals,     width=w, label="T→R (exp06)",    color="#3498db", alpha=0.8)
        ax.bar(x + w, human_vals,  width=w, label="Human",          color="#2ecc71", alpha=0.8)

        ax.set_xticks(x)
        ax.set_xticklabels([d[:8] for d in DIMENSIONS], rotation=30, ha="right", fontsize=8)
        ax.set_ylim(0, 110)
        ax.axhline(50, color="gray", linestyle="--", lw=0.8)
        ax.set_title(f"Language: {lang}", fontsize=11, fontweight="bold")
        ax.set_ylabel("% Choosing Positive Option", fontsize=9)

    axes[0].legend(fontsize=8)
    fig.suptitle("Exp06 T→R: Native vs Translated-to-English vs Human", fontsize=13, fontweight="bold")
    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"  Comparison bar chart saved → {output_path}")


