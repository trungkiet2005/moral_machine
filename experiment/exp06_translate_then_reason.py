def load_human_preferences(path: str = HUMAN_BY_LANG_PATH) -> pd.DataFrame:
    df = pd.read_csv(path)
    return df.melt(id_vars=["Label"], var_name="lang", value_name="human_pct")


def load_dataset(lang: str, max_rows: Optional[int] = None) -> pd.DataFrame:
    path = os.path.join(DATASETS_DIR, f"dataset_{lang}+google.csv")
    df = pd.read_csv(path)
    if max_rows is not None:
        df = df.head(max_rows)
    return df


def build_scenario_text(row: pd.Series) -> str:
    return str(row.get("ScenarioText", row.get("Scenario", "")))


# ══════════════════════════════════════════════════════════════════════════════
# SECTION 4: STEP 1 — TRANSLATE TO ENGLISH
# ══════════════════════════════════════════════════════════════════════════════

def translate_batch(
    model,
    tokenizer,
    texts: List[str],
    source_lang: str,
    batch_size: int = 4,
) -> Tuple[List[str], List[float]]:
    """
    Translate a list of scenario texts to English using the model itself.

    Returns:
      translations     — English text per scenario
      length_ratios    — len(translation) / len(original) per scenario
                         proxy for translation coverage (ratio << 1 = truncated)
    """
    translations  : List[str]   = []
    length_ratios : List[float] = []

    for i in range(0, len(texts), batch_size):
        batch = texts[i : i + batch_size]

        prompts = []
        for text in batch:
            messages = [
                {"role": "system", "content": TRANSLATION_SYSTEM},
                {"role": "user",   "content": f"Translate to English:\n\n{text}"},
            ]
            prompts.append(
                tokenizer.apply_chat_template(
                    messages, tokenize=False, add_generation_prompt=True
                )
            )

        inputs = tokenizer(
            prompts,
            return_tensors="pt",
            padding=True,
            truncation=True,
            max_length=1024,
        ).to(DEVICE)

        with torch.no_grad():
            out = model.generate(
                **inputs,
                max_new_tokens=CONFIG["translation_max_tokens"],
                do_sample=False,
                temperature=None,
                top_p=None,
                pad_token_id=tokenizer.pad_token_id,
            )

        input_len = inputs["input_ids"].shape[1]
        for j, seq in enumerate(out):
            new_tokens = seq[input_len:]
            translated = tokenizer.decode(new_tokens, skip_special_tokens=True).strip()
            translations.append(translated)
            ratio = len(translated) / max(len(batch[j]), 1)
            length_ratios.append(ratio)

    return translations, length_ratios


# ══════════════════════════════════════════════════════════════════════════════
# SECTION 5: STEP 2 — FORCED DECISION ON ENGLISH TEXT
# ══════════════════════════════════════════════════════════════════════════════

def get_decision_logits_batch(
    model,
    tokenizer,
    english_texts: List[str],
    batch_size: int = 8,
) -> List[int]:
    """
    For each English scenario text (post-translation), force prefix
    "I choose Option " and read logit for "1" vs "2".
    """
    id_1 = tokenizer.encode("1", add_special_tokens=False)[0]
    id_2 = tokenizer.encode("2", add_special_tokens=False)[0]

    choices: List[int] = []

    for i in range(0, len(english_texts), batch_size):
        batch = english_texts[i : i + batch_size]

        prompts = []
        for text in batch:
            messages = [
                {"role": "system", "content": DECISION_SYSTEM},
                {"role": "user",   "content": text},
            ]
            base = tokenizer.apply_chat_template(
                messages, tokenize=False, add_generation_prompt=True
            )
            prompts.append(base + "I choose Option ")

        inputs = tokenizer(
            prompts,
            return_tensors="pt",
            padding=True,
            truncation=True,
            max_length=1536,
        ).to(DEVICE)

        with torch.no_grad():
            out = model(**inputs)

        last_logits = out.logits[:, -1, :]
        for k in range(len(batch)):
            choice = 1 if last_logits[k, id_1].item() >= last_logits[k, id_2].item() else 2
            choices.append(choice)

    return choices


# ══════════════════════════════════════════════════════════════════════════════
# SECTION 6: PREFERENCE AGGREGATION & METRICS
# ══════════════════════════════════════════════════════════════════════════════

def aggregate_preferences(df: pd.DataFrame, choices: List[int]) -> pd.DataFrame:
    df = df.copy()
    df["tr_choice"] = choices
    records = []
    for dim in DIMENSIONS:
        if "ScenarioType" in df.columns:
            subset = df[df["ScenarioType"] == dim]
        else:
            subset = df[df["Label"] == dim]
        if subset.empty:
            continue
        pos_col = "ChoosePositive" if "ChoosePositive" in df.columns else None
        if pos_col:
            correct = (
                ((subset["tr_choice"] == 1) & (subset[pos_col] == 1)) |
                ((subset["tr_choice"] == 2) & (subset[pos_col] == 0))
            )
            pct = correct.mean() * 100
        else:
            pct = (subset["tr_choice"] == 1).mean() * 100
        records.append({"Label": dim, "tr_pct": pct})
    return pd.DataFrame(records)


def compute_metrics(
    pref_df: pd.DataFrame,
    human_df: pd.DataFrame,
    lang: str,
) -> Dict:
    human_lang = human_df[human_df["lang"] == lang].set_index("Label")["human_pct"]
    metrics = {}
    for _, row in pref_df.iterrows():
        dim = row["Label"]
        if dim not in human_lang.index:
            continue
        h = human_lang[dim] / 100.0
        m = row["tr_pct"] / 100.0
        p = np.array([m, 1 - m])
        q = np.array([h, 1 - h])
        metrics[dim] = {
            "jsd": float(jensenshannon(p, q) ** 2),
            "mae": abs(m * 100 - h * 100),
        }
    if metrics:
        metrics["_mean_jsd"] = np.mean([v["jsd"] for v in metrics.values()])
        metrics["_mean_mae"] = np.mean([v["mae"] for v in metrics.values()])
    return metrics


def compute_recovery_scores(
    tr_pref: Dict[str, Dict[str, float]],
    native_pref: Dict[str, Dict[str, float]],
    en_pref: Dict[str, float],
) -> Dict[str, Dict[str, float]]:
    """
    Recovery(l, d) = 1 - |T→R_pref(l,d) - en_pref(d)| / (|native_pref(l,d) - en_pref(d)| + ε)

    Measures: how much of the gap between native language and English is
    recovered by translating to English before querying.
    1.0 = T→R output matches English perfectly (full recovery)
    0.0 = T→R output == native output (no effect from translation)
    """
    eps = 1e-6
    recovery: Dict[str, Dict[str, float]] = {}
    for lang in tr_pref:
        if lang == "en":
            continue
        recovery[lang] = {}
        for dim in DIMENSIONS:
            tr_val  = tr_pref.get(lang, {}).get(dim, float("nan"))
            nat_val = native_pref.get(lang, {}).get(dim, float("nan"))
            en_val  = en_pref.get(dim, float("nan"))
            if any(np.isnan(v) for v in [tr_val, nat_val, en_val]):
                recovery[lang][dim] = float("nan")
                continue
            gap_nat  = abs(nat_val - en_val)
            gap_tr   = abs(tr_val  - en_val)
            recovery[lang][dim] = 1.0 - gap_tr / (gap_nat + eps)
    return recovery


def compute_clag(lang_pref_map: Dict[str, Dict[str, float]]) -> float:
    langs = list(lang_pref_map.keys())
    dims  = list(next(iter(lang_pref_map.values())).keys())
    total = 0.0
    for l1 in langs:
        for l2 in langs:
            for d in dims:
                if d in lang_pref_map[l1] and d in lang_pref_map[l2]:
                    total += abs(lang_pref_map[l1][d] - lang_pref_map[l2][d])
    return total / len(langs) ** 2 if langs else 0.0


# ══════════════════════════════════════════════════════════════════════════════
# SECTION 7: PER-LANGUAGE EVAL LOOP
# ══════════════════════════════════════════════════════════════════════════════

def run_tr_eval(
    lang: str,
    model,
    tokenizer,
    human_df: pd.DataFrame,
    max_rows: Optional[int] = None,
    batch_size: int = 4,
) -> Tuple[pd.DataFrame, Dict, List[str], List[float]]:
    """Full T→R evaluation for a single language."""
    print(f"\n[Lang: {lang}] Loading dataset …")
    df = load_dataset(lang, max_rows)
    scenario_texts = [build_scenario_text(row) for _, row in df.iterrows()]

    if lang in SKIP_TRANSLATION_FOR:
        print(f"  Skipping translation (already English).")
        english_texts = scenario_texts
        translations  = scenario_texts
        length_ratios = [1.0] * len(scenario_texts)
    else:
        print(f"  Step 1: Translating {len(scenario_texts)} scenarios to English …")
        translations, length_ratios = translate_batch(
            model, tokenizer, scenario_texts, lang, batch_size
        )
        english_texts = translations

    print(f"  Step 2: Forced decision on English text …")
    choices = get_decision_logits_batch(model, tokenizer, english_texts, batch_size)

    pref_df = aggregate_preferences(df, choices)
    metrics = compute_metrics(pref_df, human_df, lang)

    print(
        f"  [{lang}] Mean JSD={metrics.get('_mean_jsd', 0):.4f}  "
        f"MAE={metrics.get('_mean_mae', 0):.2f}pp  "
        f"Length ratio={np.mean(length_ratios):.2f}  N={len(scenario_texts)}"
    )
    return pref_df, metrics, translations, length_ratios


