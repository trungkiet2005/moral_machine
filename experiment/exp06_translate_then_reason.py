# SECTION 9: MAIN
# ══════════════════════════════════════════════════════════════════════════════

def main():
    torch.manual_seed(CONFIG["seed"])
    np.random.seed(CONFIG["seed"])

    tokenizer, model = load_llm(MODEL_NAME)
    human_df = load_human_preferences()

    all_tr_pref:     Dict[str, Dict[str, float]] = {}
    all_tr_metrics:  Dict[str, Dict]             = {}
    all_translations: Dict[str, List[str]]       = {}
    all_length_ratios: Dict[str, List[float]]    = {}

    for lang in LANGS_TO_EVAL:
        pref_df, metrics, translations, length_ratios = run_tr_eval(
            lang       = lang,
            model      = model,
            tokenizer  = tokenizer,
            human_df   = human_df,
            max_rows   = MAX_ROWS_PER_LANG,
            batch_size = 4,
        )
        all_tr_pref[lang]       = pref_df.set_index("Label")["tr_pct"].to_dict()
        all_tr_metrics[lang]    = metrics
        all_translations[lang]  = translations
        all_length_ratios[lang] = length_ratios

    # ── Load native (exp01) results for recovery computation ──────────────────
    native_pref: Dict[str, Dict[str, float]] = {}
    if os.path.exists(EXP01_RESULTS_PATH):
        with open(EXP01_RESULTS_PATH) as f:
            exp01 = json.load(f)
        native_pref = exp01.get("lang_pref", {})
        print(f"\nLoaded exp01 native preferences from {EXP01_RESULTS_PATH}")
    else:
        print(f"\nWarning: {EXP01_RESULTS_PATH} not found — skipping Recovery Score.")

    en_pref = all_tr_pref.get("en", {})

    # ── Recovery scores ───────────────────────────────────────────────────────
    recovery: Dict[str, Dict[str, float]] = {}
    if native_pref:
        recovery = compute_recovery_scores(all_tr_pref, native_pref, en_pref)

    # ── Summary table ──────────────────────────────────────────────────────────
    print("\n" + "=" * 70)
    print("EXP06 SUMMARY — Translate-then-Reason (T→R) Alignment")
    print("=" * 70)
    print(f"{'Lang':<8}  {'JSD':>8}  {'MAE(pp)':>9}  {'AvgLenRatio':>12}")
    print("-" * 42)
    jsd_vals, mae_vals = [], []
    for lang in LANGS_TO_EVAL:
        m    = all_tr_metrics.get(lang, {})
        jsd  = m.get("_mean_jsd", float("nan"))
        mae  = m.get("_mean_mae", float("nan"))
        lrat = np.mean(all_length_ratios.get(lang, [1.0]))
        jsd_vals.append(jsd)
        mae_vals.append(mae)
        print(f"{lang:<8}  {jsd:>8.4f}  {mae:>9.2f}  {lrat:>12.2f}")
    print("-" * 42)
    print(f"{'MEAN':<8}  {np.nanmean(jsd_vals):>8.4f}  {np.nanmean(mae_vals):>9.2f}")

    # ── CLAG ──────────────────────────────────────────────────────────────────
    clag_tr = compute_clag(all_tr_pref)
    print(f"\nT→R CLAG (Cross-Lingual Alignment Gap) = {clag_tr:.4f}")

    # ── Recovery scores ───────────────────────────────────────────────────────
    if recovery:
        print(f"\n{'Lang':<8}  {'Avg Recovery':>14}")
        print("-" * 25)
        for lang in LANGS_TO_EVAL:
            if lang in recovery:
                avg_rec = np.nanmean(list(recovery[lang].values()))
                print(f"{lang:<8}  {avg_rec:>14.3f}")

    # ── Dimension summary ─────────────────────────────────────────────────────
    print(f"\n{'Dimension':<18}  {'T→R Mean%':>10}  {'Human Mean%':>12}  {'Δ':>8}")
    print("-" * 55)
    human_mean = human_df.groupby("Label")["human_pct"].mean()
    for dim in DIMENSIONS:
        tr_vals  = [v.get(dim, float("nan")) for v in all_tr_pref.values()]
        tr_mean  = np.nanmean(tr_vals)
        h_mean   = human_mean.get(dim, float("nan"))
        delta    = tr_mean - h_mean
        print(f"{dim:<18}  {tr_mean:>10.1f}  {h_mean:>12.1f}  {delta:>+8.1f}")

    # ── Save results ──────────────────────────────────────────────────────────
    results = {
        "config":       CONFIG,
        "metrics":      all_tr_metrics,
        "clag_tr":      clag_tr,
        "lang_pref":    all_tr_pref,
        "recovery":     {
            lang: {k: (v if not np.isnan(v) else None) for k, v in dims.items()}
            for lang, dims in recovery.items()
        },
        "avg_length_ratios": {
            lang: float(np.mean(ratios))
            for lang, ratios in all_length_ratios.items()
        },
    }
    with open("exp06_results.json", "w") as f:
        json.dump(results, f, indent=2)
